package com.example;

/**
 * Example of UpperEll code.
 *
 * @author example
 * @since 1.0.0
 */
public class UpperEllExample {

    /**
     * Example of UpperEll code.
     */
    public void example() {
        // Lower case 'l' is used as the suffix of an integer literal (incorrect).
        long bad = 1l;

        // Upper case 'L' is used as the suffix of an integer literal (OK).
        long good = 1L;
    }
}

